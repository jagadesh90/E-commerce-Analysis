---------- PART 3: High-Value Business Insights

-- Q21. Find customers who placed more than 5 orders.
SELECT c.customer_name,COUNT(o.order_id) AS total_orders
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.customer_name
HAVING COUNT(o.order_id) > 5;

-- Q22. Find top 5 customers based on total spending.
SELECT c.customer_name,SUM(oi.quantity * oi.unit_price) AS total_spending
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
JOIN order_items oi
ON o.order_id = oi.order_id
GROUP BY c.customer_name
ORDER BY total_spending DESC
LIMIT 5;

-- Q23. Find products sold more than 30 times.
SELECT p.product_name,SUM(o.quantity) AS total_quantity_sold
FROM products p
JOIN order_items o
ON p.product_id = o.product_id
GROUP BY p.product_name
HAVING SUM(o.quantity) > 30;

