        FROM orders o
        JOIN order_items oi
        ON o.order_id = oi.order_id
        GROUP BY o.customer_id) AS avg_table);
        
-- Q29. Find products contributing more than 10% of company revenue.
SELECT p.product_name,SUM(o.quantity * o.unit_price) AS product_revenue
FROM products p
JOIN order_items o
ON p.product_id = o.product_id
GROUP BY p.product_name
HAVING SUM(o.quantity * o.unit_price) >
(SELECT SUM(quantity * unit_price) * 0.10
FROM order_items);

-- Q30. Find the city generating the highest revenue.
