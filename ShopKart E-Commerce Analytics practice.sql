---------- PART 2: Revenue & Sales Analysis

-- Q11. Find total revenue generated by each category.
select category,sum(price) as Total_Revenue
from products
group by category;

-- Q12. Find total quantity sold for each product.
select p.product_name,sum(o.quantity) as Total_quantity_sold
from products p
join order_items o
on p.product_id = o.product_id
group by p.product_name;

-- Q13. Find total orders under each order status.
